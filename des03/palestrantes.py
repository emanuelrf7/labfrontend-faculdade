class PalestrantesFisica:
    def __init__(self, id, img, name, biografy):
        self.__id = id
        self.__img = img
        self.__name = name
        self.__biografy = biografy
    
    def get_id(self):
        return self.__id
    
    def get_img(self):
        return self.__img
    
    def get_name(self):
        return self.__name
    
    def get_biografy(self):
        return self.__biografy


